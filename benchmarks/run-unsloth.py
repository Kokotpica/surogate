from unsloth import FastLanguageModel

from datasets import load_dataset
from trl import SFTTrainer, SFTConfig

from unsloth.chat_templates import standardize_sharegpt

model, tokenizer = FastLanguageModel.from_pretrained(
    model_name="Qwen/Qwen3-0.6B",
    max_seq_length=2048,  # Context length - can be longer, but uses more memory
    load_in_4bit=True,  # 4bit uses much less memory
    load_in_fp8=False,  # A bit more accurate, uses 2x memory
    full_finetuning=False,  # We have full finetuning now!
    float8_kv_cache=False,
    fast_inference=False,
    use_exact_model_name=False
    # token = "hf_...",      # use one if using gated models
)

model = FastLanguageModel.get_peft_model(
    model,
    r=16,  # Choose any number > 0! Suggested 8, 16, 32, 64, 128
    target_modules=["q_proj", "k_proj", "v_proj", "o_proj",
                    "gate_proj", "up_proj", "down_proj", ],
    lora_alpha=32,  # Best to choose alpha = rank or rank*2
    lora_dropout=0,  # Supports any, but = 0 is optimized
    bias="none",  # Supports any, but = "none" is optimized
    random_state=3407,
    use_rslora=False,  # We support rank stabilized LoRA
    loftq_config=None,  # And LoftQ
)

dataset = load_dataset("mlabonne/FineTome-100k", split="train").select(range(10000))
dataset = standardize_sharegpt(dataset)


def format_prompts(examples):
    convos = examples["conversations"]
    texts = [tokenizer.apply_chat_template(convo, tokenize=False) for convo in convos]
    return {"text": texts, }


dataset = dataset.map(format_prompts, batched=True)

trainer = SFTTrainer(
    model=model,
    tokenizer=tokenizer,
    train_dataset=dataset,
    eval_dataset=None,  # Can set up evaluation!
    args=SFTConfig(
        dataset_text_field="text",
        per_device_train_batch_size=2,
        gradient_accumulation_steps=4,  # Use GA to mimic batch size!
        warmup_steps=5,
        num_train_epochs = 1, # Set this for 1 full training run.
        # max_steps=30,
        learning_rate=2e-4,  # Reduce to 2e-5 for long training runs
        logging_steps=1,
        optim="adamw_8bit",
        weight_decay=0.001,
        lr_scheduler_type="linear",
        seed=3407,
        report_to="none",  # Use TrackIO/WandB etc
        packing=True,
        max_length=2048
    ),
)
trainer_stats = trainer.train()
